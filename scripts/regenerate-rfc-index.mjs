#!/usr/bin/env node
/**
 * RFC INDEX generator — thin wrapper over shared utils in regenerate-adr-index.mjs.
 * (wave-167 D7: ~15 LOC wrapper, reuses extractBoldField + table-builder from ADR script)
 *
 * Globs docs/decisions/ADR-*.md, filters to Status=Proposed with a Comment period
 * field present, and writes docs/decisions/_RFC_INDEX.md.
 *
 * Usage:
 *   node scripts/regenerate-rfc-index.mjs        # write docs/decisions/_RFC_INDEX.md
 *   node scripts/regenerate-rfc-index.mjs --dry  # print table to stdout, do not write
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { extractBoldField, extractTitle, extractAdrNumber } from './regenerate-adr-index.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DECISIONS_DIR = path.join(ROOT, 'docs/decisions');
const OUT = path.join(DECISIONS_DIR, '_RFC_INDEX.md');
const EXCLUDE = new Set(['_TEMPLATE.md', 'README.md', '_INDEX.md', '_RFC_INDEX.md']);
const isDry = process.argv.includes('--dry');

const files = fs.readdirSync(DECISIONS_DIR)
  .filter((f) => f.endsWith('.md') && !EXCLUDE.has(f) && f.startsWith('ADR-'))
  .sort((a, b) => parseInt((a.match(/^ADR-(\d+)/) || [])[1] ?? '0', 10) - parseInt((b.match(/^ADR-(\d+)/) || [])[1] ?? '0', 10));

const rows = [];
for (const file of files) {
  const content = fs.readFileSync(path.join(DECISIONS_DIR, file), 'utf8');
  const statusShort = (extractBoldField(content, 'Status') ?? '').split(/[|,(]/)[0].trim();
  const commentPeriod = extractBoldField(content, 'Comment period');
  if (statusShort !== 'Proposed' || !commentPeriod || commentPeriod === '—') continue;
  const num = extractAdrNumber(file);
  const title = extractTitle(content) ?? file.replace(/\.md$/, '');
  const commenters = extractBoldField(content, 'Named commenters') ?? '—';
  rows.push({ num, file, title, commentPeriod, commenters });
}

const header = '| ADR # | Title | Comment period | Named commenters |';
const sep    = '|---|---|---|---|';
const dataRows = rows.map((r) => {
  const t = r.title.length > 55 ? r.title.slice(0, 52) + '...' : r.title;
  return `| ADR-${r.num} [ADR-${r.num}](${r.file}) | ${t} | ${r.commentPeriod} | ${r.commenters} |`;
});
const table = [header, sep, ...(dataRows.length ? dataRows : ['| — | No in-flight RFCs | — | — |'])].join('\n');
const output = `# RFC Index — In-flight Proposed ADRs\n\n> Auto-generated by \`npm run rfc:regen\` — do not edit manually. Last update: ${new Date().toISOString()}\n>\n> Lists all ADRs at Status "Proposed" with a Comment period field.\n> An RFC comment period expires 48 hours after the start date; no objection = implicit accept.\n> See \`docs/decisions/README.md\` §RFC gate for the full policy.\n\n${table}\n`;

if (isDry) { process.stdout.write(output); process.exit(0); }
fs.writeFileSync(OUT, output);
process.stdout.write(`[rfc-index] wrote ${rows.length} row(s) → docs/decisions/_RFC_INDEX.md\n`);
