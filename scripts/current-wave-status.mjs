#!/usr/bin/env node
/**
 * Wave-57 — GSD `STATE.md` pattern absorbed (live progress doc).
 *
 * Writes docs/CURRENT_WAVE.md with a snapshot of where we are: latest
 * wave from git log + dashboard, latest retro filename, open audit
 * findings, outcome status, last 5 commits. ONE FILE for "what's the
 * current state of the dev system" instead of digging through retros +
 * dashboard + audit reports.
 *
 * Wired to post-commit hook so it auto-updates on every wave commit.
 *
 * Usage:
 *   node scripts/current-wave-status.mjs        # rewrite docs/CURRENT_WAVE.md
 *   node scripts/current-wave-status.mjs --dry  # print, don't write
 */

import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'docs/CURRENT_WAVE.md');
const isDry = process.argv.includes('--dry');

const sh = (cmd) => {
  try {
    return execSync(cmd, { cwd: ROOT, encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
};

// ── Latest wave (from git log subject) ─────────────────────────────
const latestWave = sh("git log --pretty=%s -50 | grep -oE 'wave-[0-9]+(\\.[0-9]+[a-z]?)?' | sort -u | sort -t- -k2 -n | tail -1") || 'unknown';

// ── Latest retro file ──────────────────────────────────────────────
const latestRetro = sh("ls -1 docs/retros/wave-*.md 2>/dev/null | sort -V | tail -1") || '(none)';

// ── Open audit findings (from backlog banner --machine-readable hack) ─
const banner = sh('bash scripts/audit-backlog-status.sh');
const openMatch = banner.match(/open\s+:\s+(\d+)/);
const escMatch = banner.match(/escalated.*:\s+(\d+)/);
const open = openMatch ? openMatch[1] : '?';
const escalated = escMatch ? escMatch[1] : '?';

// ── Outcomes status ────────────────────────────────────────────────
let outcomesSummary = '(outcome-check not available)';
try {
  const outcomesJson = JSON.parse(execSync('node scripts/outcome-check.mjs --json 2>/dev/null', { cwd: ROOT, encoding: 'utf8' }) || '{}');
  if (outcomesJson.results) {
    const met = outcomesJson.results.filter((r) => r.met).length;
    const total = outcomesJson.results.length;
    outcomesSummary = `${met} / ${total} met`;
  }
} catch { /* leave as default */ }

// ── Last 5 commits ─────────────────────────────────────────────────
const lastCommits = sh("git log --pretty='%h  %s' -5") || '(no commits)';

// ── Last design drift snapshot ─────────────────────────────────────
let driftSummary = '(no snapshot)';
try {
  const baseline = JSON.parse(fs.readFileSync(path.join(ROOT, 'scripts/.design-drift-baseline.json'), 'utf8'));
  driftSummary = `heights ${baseline.raw_control_heights} · text-px ${baseline.raw_text_px} · tinted-files ${baseline.tinted_surface_files}`;
} catch { /* leave default */ }

// ── Build markdown ─────────────────────────────────────────────────
const content = `# Current Wave Status

> Auto-generated by \`scripts/current-wave-status.mjs\` on every commit. Do not edit manually — your changes will be overwritten.
> Last update: ${new Date().toISOString().replace('T', ' ').slice(0, 19)} UTC

## Where we are

- **Latest wave**: \`${latestWave}\`
- **Latest retro**: \`${latestRetro}\`
- **Audit backlog**: ${open} open · ${escalated} escalated
- **Outcomes**: ${outcomesSummary}
- **Design drift baseline**: ${driftSummary}

## Last 5 commits

\`\`\`
${lastCommits}
\`\`\`

## Where to look next

| What | Where |
|---|---|
| Full audit backlog | \`npm run audit:backlog\` |
| Outcome details | \`npm run outcome:check\` |
| Design drift breakdown | \`npm run check:design-drift\` |
| Latest weekly routine report | \`docs/audit-reports/routine-weekly-*.md\` (latest) |
| All retros (last 5) | \`ls -t docs/retros/*.md \\| head -5\` |
| Skills registry | \`.claude/skills/_INDEX.md\` |
| Agent roster | \`docs/AGENT_ROSTER.md\` |
| Cadence chart | \`docs/AGENT_ROSTER.md\` (5 cadences section) |

## Banner output (latest)

\`\`\`
${banner.replace(/\[[0-9;]*m/g, '').split('\n').slice(0, 18).join('\n')}
\`\`\`
`;

if (isDry) {
  process.stdout.write(content);
  process.exit(0);
}

fs.writeFileSync(OUT, content);
console.log(`✓ Wrote ${path.relative(ROOT, OUT)}`);
